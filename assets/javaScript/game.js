$(document).ready(function() {

    //variables

    var minNum = 9;       //Lowest number generated
    var maxNum = 120;     //Highest number generated
    var random;           //jQuery generated random number
    var win = 0;          //Number of games won
    var lose = 0;         //Number of games lost
    var jewel1 = 12;      //First jewel
    var jewel2 = 4;       //Second jewel
    var jewel3 = 1;       //Third jewel
    var jewel4 = 9;       //Fourth jewel
    var scoreSum = 0;     //sum of numbers generated by jewels

    
    //Generate a random number and append it to <span id="ranNum">
    var getNumber = function() {
        random = Math.floor(Math.random() * (maxNum - minNum + 1) + minNum);
        $("#ranNum").html(random);
    }

    getNumber();

    //Generate a number when a jewel is clicked and add it to the 
    //collective score
    
    $("#jewel1").click(function() {
        scoreSum = jewel1 + scoreSum;
        $("#scoreNum").html(scoreSum);
        checkScore();
    })


    $("#jewel2").click(function() {
        scoreSum = jewel2 + scoreSum;
        $("#scoreNum").html(scoreSum);
        checkScore();
    })

    $("#jewel3").click(function() {
        scoreSum = jewel3 + scoreSum;
        $("#scoreNum").html(scoreSum);
        checkScore();
    })

    $("#jewel4").click(function() {
        scoreSum = jewel4 + scoreSum;
        $("#scoreNum").html(scoreSum);
        checkScore();
    })

    //Finish game once score is achieved or surpassed

    var checkScore = function() {
    if (scoreSum === random) {
        win++;
        $("#wins").html(win);
        scoreSum = 0;
        $("#scoreNum").html(scoreSum);
        getNumber();
    } else if (scoreSum > random) {
        lose++;
        $("#losses").html(lose);
        scoreSum = 0;
        $("#scoreNum").html(scoreSum);
        getNumber();
    }
}


    

})
